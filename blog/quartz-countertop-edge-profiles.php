                <h2>Frequently Asked Questions</h2>

                <div class="faq-item">
                    <h3>What is the most popular countertop edge profile?</h3>
                    <p>The eased (straight) edge is the most popular countertop edge profile overall, especially in modern and contemporary kitchens. The half bullnose is a close second and is favored in transitional and family-friendly homes. These profiles are popular because they're versatile, affordable, comfortable, and work with virtually any kitchen style.</p>
                </div>

                <div class="faq-item">
                    <h3>What countertop edge is best for a modern kitchen?</h3>
                    <p>For modern kitchens, the eased (straight) edge, beveled edge, mitered edge, and waterfall edge are the best choices. These profiles feature clean, geometric lines that complement flat-panel cabinetry and minimalist design. The mitered and waterfall edges create a particularly dramatic, contemporary statement.</p>
                </div>

                <div class="faq-item">
                    <h3>How much more does a waterfall edge cost?</h3>
                    <p>A waterfall edge is the most expensive edge option because it requires additional slab material for the vertical panel, precision 45-degree miter cuts, and careful pattern alignment at the seam. The cost varies depending on island size and material, but expect to add $1,500-$5,000+ to your project cost compared to a standard edge. The exact amount depends on the height of the waterfall, the material selected, and the complexity of pattern matching.</p>
                </div>

                <div class="faq-item">
                    <h3>Does the edge profile affect the durability of quartz countertops?</h3>
                    <p>The edge profile has a minor effect on durability. Edges with thin or protruding features (like the delicate curves of an ogee) are slightly more susceptible to chipping from heavy impacts than solid, rounded profiles (like a full bullnose). However, quartz is very chip-resistant overall, and edge damage from normal household use is rare regardless of profile. The choice should be driven primarily by aesthetics and comfort.</p>
                </div>

                <div class="faq-item">
                    <h3>Can I have different edge profiles on different sections of my countertop?</h3>
                    <p>Yes, absolutely. Many homeowners use a simpler edge (eased or half bullnose) on their perimeter countertops and a more dramatic edge (mitered or waterfall) on their kitchen island. This creates visual hierarchy, draws attention to the island as a focal point, and helps manage costs by reserving the premium edge for the most visible area.</p>
